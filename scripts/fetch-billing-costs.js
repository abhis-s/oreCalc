const fs = require('fs');
const path = require('path');

const outputFile = path.join(__dirname, '../js/data/runningCostsData.js');

function main() {
    const defaultData = {
        lastUpdated: new Date().toISOString(),
        isMock: true,
        totalCostTillDate: 0,
        breakdown: [
            {
                month: "2026-05",
                totalCost: 37.00,
                services: [
                    { name: "Compute Engine", cost: 9.50 },
                    { name: "Networking", cost: 27.50 }
                ]
            }
        ]
    };

    try {
        const dir = path.dirname(outputFile);
        if (!fs.existsSync(dir)) {
            fs.mkdirSync(dir, { recursive: true });
        }
        const content = `// This file is auto-generated at build time by scripts/fetch-billing-costs.js.
// Do not edit this file directly.

export const runningCostsData = ${JSON.stringify(defaultData, null, 4)};
`;
        fs.writeFileSync(outputFile, content, 'utf8');
        console.log('Successfully wrote default fallback running costs data.');
    } catch (err) {
        console.error('Error writing default running costs data:', err);
    }
}

main();
